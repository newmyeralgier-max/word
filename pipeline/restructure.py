_NUM_RE = re.compile(r"^\s*\d+(?:\.\d+){0,3}\.?\s+")


def _normalize(s: str) -> str:
    """Нормализация для поиска подстроки: lower + ё→е, CamelCase-split,
    убрать номер в начале, и удалить ВСЕ пробелы. Так сравнение становится
    устойчивым к слипшимся словам ('ПараметрынастройкиблокаPowergui')."""
    s = s.strip()
    s = _NUM_RE.sub("", s)
    s = re.sub(r"([a-z])([A-Z])", r"\1 \2", s)
    s = re.sub(r"([а-яё])([A-Z])", r"\1 \2", s)
    s = re.sub(r"([A-Z])([A-Z][a-z])", r"\1 \2", s)
    s = s.lower().replace("ё", "е")
    # удаляем ВСЕ не-буквенно-цифровые, включая пробелы — иначе русские слова
    # в исходнике могут быть склеены («параметрынастройкиблока»), а ключ
    # записан с пробелами.
    s = re.sub(r"[^a-zа-я0-9]", "", s)
    return s


_TOC_TAIL_RE = re.compile(r"\t\s*\d{1,3}\s*$")


def _is_toc_paragraph(p) -> bool:
    """TOC-запись определяем по:
    - стилю, начинающемуся на «toc»
    - или по tab+номер_страницы в конце текста (типичный шаблон TOC).
    """
    try:
        style_name = (p.style.name or "").lower() if p.style else ""
    except Exception:
        style_name = ""
    if style_name.startswith("toc") or style_name.startswith("оглавление"):
        return True
    # XML-проверка на PAGEREF / hyperlink на закладку — тоже признак TOC
    xml = p._element
    if xml.findall(".//" + qn("w:instrText")) or xml.findall(".//" + qn("w:hyperlink")):
        # оглавление-ссылки, не настоящий заголовок
        return True
    txt = p.text or ""
    if _TOC_TAIL_RE.search(txt):
        return True
    return False


def _strip_toc_zone(doc) -> int:
    """Удалить TOC-зону в начале body.

    Сценарий: title-zone обрывается ПОСЛЕ «СОДЕРЖАНИЕ», значит первые
    параграфы body — это устаревший TOC. Идём до первого появления
    «ВВЕДЕНИЕ» (или любого абзаца-признака начала тела) и удаляем всё
    перед ним. Пользователь регенерирует TOC в Word через ПКМ →
    «Обновить поле».
    """
    paragraphs = list(doc.paragraphs)
    end_idx = None
    for i, p in enumerate(paragraphs):
        if i > 200:
            break
        raw = p.text or ""
        t = raw.strip().upper()
        if not t:
            continue
        # Пропускаем явные TOC-абзацы (стиль toc N или «текст\tномер_стр»)
        try:
            sn = (p.style.name or "").lower() if p.style else ""
        except Exception:
            sn = ""
        if sn.startswith("toc") or sn.startswith("оглавление"):
            continue
        if "\t" in raw and re.search(r"\t\s*\d{1,3}\s*$", raw):
            continue
        # «1.3. Запуск ... 12» — TOC без tab, страница в конце
        if re.search(r"\D\d{1,3}\s*$", raw) and re.match(r"^\d", raw):
            continue
        # Это реально начало тела — ВВЕДЕНИЕ или CAPS-заголовок главы 1
        if t.startswith("ВВЕДЕНИЕ"):
            end_idx = i
            break
        if (t.startswith("1.") or t.startswith("1 ")) and \
                len(t) < 80 and sum(c.isupper() for c in t if c.isalpha()) > 5:
            end_idx = i
            break
    if end_idx is None or end_idx == 0:
        return 0
    removed = 0
    for p in paragraphs[:end_idx]:
        el = p._element
        if el.getparent() is not None:
            el.getparent().remove(el)
            removed += 1
    return removed


def restructure(body_in: str, body_out: str) -> dict:
    doc = Document(body_in)

    # 1) Сначала убираем TOC-зону
    toc_removed = _strip_toc_zone(doc)

    # Нормализуем ключи в том же формате (без пробелов)
    queue = [(_normalize(k), nt, k) for (k, nt) in PLAN]
    paragraphs = list(doc.paragraphs)
    applied = 0
    applied_log: List[Tuple[str, str, str]] = []
    toc_skipped = 0

    for p in paragraphs:
        if not queue:
            break
        text = p.text.strip()
        if not text:
            continue
        if _is_toc_paragraph(p):
            toc_skipped += 1
            continue
        norm = _normalize(text)
        key_norm, new_text, key_raw = queue[0]
        if key_norm and key_norm in norm:
            set_paragraph_text(p._element, new_text)
            applied_log.append((key_raw, text[:80], new_text))
            applied += 1
            queue.pop(0)

    doc.save(body_out)
    return {
        "applied": applied,
        "toc_removed": toc_removed,
        "toc_skipped": toc_skipped,
        "skipped": len(queue),
        "skipped_list": [(k, t) for (_, t, k) in queue[:20]],
        "log_sample": applied_log[:60],
    }


if __name__ == "__main__":
    if len(sys.argv) != 3:
        print("Usage: python restructure.py <body_in.docx> <body_out.docx>")
        sys.exit(1)
    r = restructure(sys.argv[1], sys.argv[2])
    print(f"[restructure] applied={r['applied']}  not_found={r['skipped']}")
    for key, text, new in r["log_sample"]:
        print(f"  {text[:50]:50}  →  {new[:70]}")
    if r["skipped_list"]:
        print("  NOT FOUND in body:")
        for k, v in r["skipped_list"]:
            print(f"    - {k!r} → {v}")
