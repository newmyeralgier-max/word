def delete_from_heading(doc, heading_text):
    found = False
    paras_to_remove = []
    for i, p in enumerate(doc.paragraphs):
        if heading_text in p.text:
            found = True
        if found:
            paras_to_remove.append(p)
    
    # Also remove tables if they are after the heading
    # This is tricky with python-docx as it separates paragraphs and tables.
    # A better way is to clear the document and recreate the new parts if we know where they are.
    # But since we are appending at the end, we can just find the index of the first para to remove.
    
    if not found:
        return False

    # For tables, it's harder. Let's just create a new document based on the existing one up to the heading.
    return True

def add_heading(doc, text, level):
    p = doc.add_heading(text, level=level)
    return p

def add_paragraph(doc, text, bold=False, italic=False, style='Normal'):
    p = doc.add_paragraph(text, style=style)
    if bold:
        if p.runs: p.runs[0].bold = True
        else: p.add_run().bold = True
    if italic:
        if p.runs: p.runs[0].italic = True
        else: p.add_run().italic = True
    return p

def add_figure(doc, img_path, caption):
    if os.path.exists(img_path):
        doc.add_picture(img_path, width=Inches(6))
        last_shape = doc.paragraphs[-1]
        last_shape.alignment = WD_ALIGN_PARAGRAPH.CENTER
    else:
        print(f"Warning: Image not found at {img_path}")
    
    p = doc.add_paragraph(caption)
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if p.runs:
        p.runs[0].bold = True
    return p

def create_table(doc, data, header_bold=True):
    rows = len(data)
    cols = len(data[0])
    table = doc.add_table(rows=rows, cols=cols)
    table.style = 'Table Grid'
    for r in range(rows):
        for c in range(cols):
            cell = table.cell(r, c)
            cell.text = str(data[r][c])
            if r == 0 and header_bold:
                for para in cell.paragraphs:
                    for run in para.runs:
                        run.bold = True
            para = cell.paragraphs[0]
            para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    return table

