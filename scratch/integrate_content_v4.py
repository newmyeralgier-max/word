def add_heading(doc, text, level):
    p = doc.add_heading(text, level=level)
    return p

def add_paragraph(doc, text, bold=False, italic=False, style='Normal', alignment=None):
    p = doc.add_paragraph(text, style=style)
    if alignment:
        p.alignment = alignment
    if bold and p.runs:
        p.runs[0].bold = True
    elif bold:
        p.add_run().bold = True
    if italic and p.runs:
        p.runs[0].italic = True
    return p

def add_figure(doc, img_path, caption):
    if os.path.exists(img_path):
        p_img = doc.add_paragraph()
        p_img.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p_img.add_run()
        run.add_picture(img_path, width=Inches(6))
    else:
        print(f"Warning: Image not found at {img_path}")
    
    p_cap = doc.add_paragraph(caption)
    p_cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    if p_cap.runs:
        p_cap.runs[0].bold = True
    return p_cap

def create_table(doc, data, header_bold=True):
    rows = len(data)
    cols = len(data[0])
    table = doc.add_table(rows=rows, cols=cols)
    table.style = 'Table Grid'
    for r in range(rows):
        for c in range(cols):
            cell = table.cell(r, c)
            cell.text = str(data[r][c])
            if r == 0 and header_bold:
                for para in cell.paragraphs:
                    for run in para.runs:
                        run.bold = True
            para = cell.paragraphs[0]
            para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    return table

